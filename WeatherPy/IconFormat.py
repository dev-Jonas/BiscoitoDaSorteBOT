def IconFormat(icon):
    match(icon):
        case '01d':
            return ':sunny: '
        case '01n':
            return ':new_moon: '
        case '02d':
            return ':white_sun_cloud: '
        case '02n':
            return ':white_sun_cloud: '
        case '03d':
            return ':cloud: ' 
        case '03n':
            return ':cloud: ' 
        case '04d':
            return ':cloud: ' 
        case '04n':
            return ':cloud: ' 
        case '09d':
            return ':cloud_rain: ' 
        case '09n':
            return ':cloud_rain: ' 
        case '10d':
            return ':white_sun_rain_cloud: ' 
        case '10n':
            return ':white_sun_rain_cloud: ' 
        case '11d':
            return ':thunder_cloud_rain: ' 
        case '11n':
            return ':thunder_cloud_rain: ' 
        case '13d':
            return ':snowflake: ' 
        case '13n':
            return ':snowflake: ' 
        case '50d':
            return ':fog: ' 
        case '50n':
            return ':fog: ' 
        
